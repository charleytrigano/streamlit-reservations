def _validate_hex(c: str) -> bool:
    return isinstance(c, str) and c.startswith("#") and len(c) in (4, 7)

def _clean_palette(pal: dict) -> dict:
    if not isinstance(pal, dict):
        return DEFAULT_PALETTE.copy()
    out = {}
    for k, v in pal.items():
        if isinstance(k, str) and _validate_hex(str(v)):
            out[k.strip()] = str(v)
    if not out:
        out = DEFAULT_PALETTE.copy()
    return out

def get_palette() -> dict:
    if not st.session_state.palette:
        st.session_state.palette = DEFAULT_PALETTE.copy()
    st.session_state.palette = _clean_palette(st.session_state.palette)
    return st.session_state.palette

def save_palette(palette: dict):
    st.session_state.palette = _clean_palette(palette)

