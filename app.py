def afficher_calendrier(df):
    st.subheader("üìÖ Calendrier")
    if df.empty or "annee" not in df.columns or df["annee"].dropna().empty:
        st.warning("Aucune ann√©e valide disponible dans les donn√©es.")
        return
    col1, col2 = st.columns(2)
